class SongModel {
    constructor(id, title, genre, releaseYear, artist, duration, link, albumId) {
      this.id = id;
      this.title = title;
      this.genre = genre;
      this.releaseYear = releaseYear;
      this.artist = artist;
      this.duration = duration;
      this.link = link;
      this.albumId = albumId;
    }
  }
  
  export default SongModel;
  